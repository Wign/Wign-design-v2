                tegnsprog – og de andre studerende er højst sandsynligt hørende. De læser altså alene hvilket forringer
                muligheden for at eventuelle ny tegn for fagtermer på de videregående uddannelser kan spredes. Det er en
                forudsætning for et sprog at blive stimuleret ved jævnlig aktivitet i det pågældende sprogmiljø.</p>
            <p>Behovet for en platform til deling af tegn var til stede i lang tid, fra dengang hvor døve begyndte at
                kunne tage tegnsprogtolk med på uddannelserne. I 1966 var Asger Bergmann den første døve som begyndte på
                en ungdomsuddannelse. Siden da har det været almindeligt for døve at genopfinde den dybe tallerken for
                give et nyt tegn til et begreb uden at vide det selvom der allerede var et andet tegn. Grunden til det
                var at der formentligt havde været en forhenværende studerende som allerede havde lavet et andet tegn
                til den selvsamme betydning, og at tegnsprogstalende i samfundet består af fragmenterede grupper som
                ikke nødvendigvis er i kontakt med hinanden.</p>
